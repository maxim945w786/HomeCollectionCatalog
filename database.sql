USE HomeCollectionCatalogDB;
GO

CREATE TABLE Roles (
    RoleID INT PRIMARY KEY IDENTITY(1,1),
    RoleName NVARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE Users (
    UserID INT PRIMARY KEY IDENTITY(1,1),
    RoleID INT NOT NULL,
    FullName NVARCHAR(150) NOT NULL,
    Email NVARCHAR(100) NOT NULL UNIQUE,
    PasswordHash NVARCHAR(255) NOT NULL,
    FOREIGN KEY (RoleID) REFERENCES Roles(RoleID)
);

CREATE TABLE ItemTypes (
    ItemTypeID INT PRIMARY KEY IDENTITY(1,1),
    TypeName NVARCHAR(100) NOT NULL UNIQUE
);

CREATE TABLE Categories (
    CategoryID INT PRIMARY KEY IDENTITY(1,1),
    CategoryName NVARCHAR(100) NOT NULL UNIQUE,
    Description NVARCHAR(255)
);

CREATE TABLE StorageLocations (
    StorageLocationID INT PRIMARY KEY IDENTITY(1,1),
    RoomName NVARCHAR(100) NOT NULL,
    PlaceName NVARCHAR(100) NOT NULL,
    StorageType NVARCHAR(100) NOT NULL
);

CREATE TABLE AcquisitionSources (
    SourceID INT PRIMARY KEY IDENTITY(1,1),
    SourceName NVARCHAR(100) NOT NULL UNIQUE
);

CREATE TABLE ItemConditions (
    ConditionID INT PRIMARY KEY IDENTITY(1,1),
    ConditionName NVARCHAR(100) NOT NULL UNIQUE
);

CREATE TABLE ItemStatuses (
    StatusID INT PRIMARY KEY IDENTITY(1,1),
    StatusName NVARCHAR(100) NOT NULL UNIQUE
);

CREATE TABLE CollectionItems (
    ItemID INT PRIMARY KEY IDENTITY(1,1),
    ItemName NVARCHAR(200) NOT NULL,
    ItemTypeID INT NOT NULL,
    CategoryID INT NOT NULL,
    StorageLocationID INT NOT NULL,
    ConditionID INT NOT NULL,
    StatusID INT NOT NULL,
    Description NVARCHAR(500),
    ProductionYear INT NULL,
    EstimatedValue DECIMAL(12,2) NOT NULL,
    IsInsured BIT NOT NULL DEFAULT 0,
    PhotoPath NVARCHAR(255),
    CreatedAt DATETIME NOT NULL DEFAULT GETDATE(),
    FOREIGN KEY (ItemTypeID) REFERENCES ItemTypes(ItemTypeID),
    FOREIGN KEY (CategoryID) REFERENCES Categories(CategoryID),
    FOREIGN KEY (StorageLocationID) REFERENCES StorageLocations(StorageLocationID),
    FOREIGN KEY (ConditionID) REFERENCES ItemConditions(ConditionID),
    FOREIGN KEY (StatusID) REFERENCES ItemStatuses(StatusID)
);

CREATE TABLE AcquisitionHistory (
    AcquisitionID INT PRIMARY KEY IDENTITY(1,1),
    ItemID INT NOT NULL,
    SourceID INT NOT NULL,
    AcquisitionDate DATE NOT NULL,
    PurchasePrice DECIMAL(12,2) NOT NULL,
    PreviousOwner NVARCHAR(150),
    Notes NVARCHAR(500),
    FOREIGN KEY (ItemID) REFERENCES CollectionItems(ItemID),
    FOREIGN KEY (SourceID) REFERENCES AcquisitionSources(SourceID)
);

CREATE TABLE Valuations (
    ValuationID INT PRIMARY KEY IDENTITY(1,1),
    ItemID INT NOT NULL,
    ValuationDate DATE NOT NULL,
    CurrentValue DECIMAL(12,2) NOT NULL,
    ExpertName NVARCHAR(150),
    Notes NVARCHAR(500),
    FOREIGN KEY (ItemID) REFERENCES CollectionItems(ItemID)
);

CREATE TABLE InsuranceDocuments (
    InsuranceID INT PRIMARY KEY IDENTITY(1,1),
    ItemID INT NOT NULL,
    PolicyNumber NVARCHAR(100) NOT NULL,
    InsuranceCompany NVARCHAR(150) NOT NULL,
    InsuredValue DECIMAL(12,2) NOT NULL,
    StartDate DATE NOT NULL,
    EndDate DATE NOT NULL,
    DocumentPath NVARCHAR(255),
    FOREIGN KEY (ItemID) REFERENCES CollectionItems(ItemID)
);

CREATE TABLE PurchasePlans (
    PlanID INT PRIMARY KEY IDENTITY(1,1),
    CategoryID INT NOT NULL,
    DesiredItemName NVARCHAR(200) NOT NULL,
    TargetPrice DECIMAL(12,2),
    PriorityLevel INT NOT NULL,
    PlannedPurchaseDate DATE,
    FOREIGN KEY (CategoryID) REFERENCES Categories(CategoryID)
);

CREATE TABLE Exhibitions (
    ExhibitionID INT PRIMARY KEY IDENTITY(1,1),
    ExhibitionName NVARCHAR(150) NOT NULL,
    Description NVARCHAR(500),
    CreatedAt DATE NOT NULL
);

CREATE TABLE ExhibitionItems (
    ExhibitionID INT NOT NULL,
    ItemID INT NOT NULL,
    DisplayOrder INT NOT NULL,
    PRIMARY KEY (ExhibitionID, ItemID),
    FOREIGN KEY (ExhibitionID) REFERENCES Exhibitions(ExhibitionID),
    FOREIGN KEY (ItemID) REFERENCES CollectionItems(ItemID)
);
GO


